# =============================================================================
# Elasticsearch 데이터 임포터
# =============================================================================
#
# 역할: PostgreSQL 매물 데이터를 Elasticsearch에 동기화
#
# 기능:
# - bulk_import(): 전체 매물 데이터 일괄 인덱싱
# - sync_single(): 단일 매물 동기화 (생성/수정/삭제)
# - create_index(): 인덱스 생성 및 매핑 적용
# - delete_index(): 인덱스 삭제 (재인덱싱용)
#
# 사용:
#   python -m data_import.importers.elasticsearch_importer
#
# 또는 main.py에서 호출
# =============================================================================

def map_style_keywords(keywords: List[str]) -> Tuple[List[str], List[str]]:
    """
    자연어 스타일 키워드를 시스템 태그로 매핑
    
    Args:
        keywords: 사용자가 입력한 스타일 키워드 리스트
                  예: ["햇살 잘 들어오는", "깔끔한"]
    
    Returns:
        Tuple[mapped_tags, unmapped_keywords]:
        - mapped_tags: 시스템 태그로 변환된 리스트 (중복 제거)
                       예: ["채광좋음", "깔끔함"]
        - unmapped_keywords: 매핑되지 않은 원본 키워드 리스트
                             (search_text 필드에서 검색용)
    
    **Feature: chatbot-landlist-integration-fix**
    **Validates: Requirements 1.1, 1.4, 8.1, 8.2, 8.3, 8.5**
    """
    mapped: List[str] = []
    unmapped: List[str] = []
    
    # 더 긴 키워드가 먼저 매칭되도록 정렬 (더 구체적인 매칭 우선)
    sorted_mapping = sorted(STYLE_MAPPING.items(), key=lambda x: len(x[0]), reverse=True)
    
    for keyword in keywords:
        if not keyword:
            continue
            
        found = False
        keyword_lower = keyword.lower().replace(" ", "")
        
        # STYLE_MAPPING에서 매칭되는 키워드 찾기 (긴 키워드 우선)
        for natural, tag in sorted_mapping:
            if natural in keyword_lower:
                if tag not in mapped:
                    mapped.append(tag)
                found = True
                break
        
        # 매핑되지 않은 키워드는 unmapped에 추가
        if not found:
            unmapped.append(keyword)
    
    return mapped, unmapped


def get_all_style_tags() -> List[str]:
    """
    시스템에서 지원하는 모든 스타일 태그 목록 반환
    
    Returns:
        중복 제거된 스타일 태그 리스트
    """
    return list(set(STYLE_MAPPING.values()))


def get_keywords_for_tag(tag: str) -> List[str]:
    """
    특정 스타일 태그에 매핑되는 모든 자연어 키워드 반환
    
    Args:
        tag: 시스템 스타일 태그 (예: "채광좋음")
    
    Returns:
        해당 태그에 매핑되는 자연어 키워드 리스트
    """
    return [keyword for keyword, mapped_tag in STYLE_MAPPING.items() if mapped_tag == tag]

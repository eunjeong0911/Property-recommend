# 역할: 프론트엔드에서 호출하는 검색 API 엔드포인트
#
# API 엔드포인트:
# - GET /api/search/listings/     : 매물 검색 (키워드 + 필터)
# - GET /api/search/autocomplete/ : 자동완성 제안
# - GET /api/search/nearby/       : 위치 기반 주변 매물 검색
#
# 쿼리 파라미터:
# - q: 검색 키워드
# - min_price, max_price: 가격 범위
# - min_area, max_area: 면적 범위
# - district: 구/동 필터
# - lat, lng, radius: 위치 기반 검색

"""
02_create_features.py
중개사 특성 기반 Feature 생성 스크립트 (안정화 버전)
"""

import pandas as pd
import numpy as np
from pathlib import Path


# ============================================================
# 1) 타겟 데이터 로드
# ============================================================
def load_target_data(filepath: str = "data/ML/office_target.csv") -> pd.DataFrame:
    file = Path(filepath)
    if not file.exists():
        raise FileNotFoundError(f"[ERROR] 파일이 존재하지 않음: {filepath}")

    print(f"📂 [1단계] 타겟 데이터 로드: {filepath}")
    df = pd.read_csv(file, encoding="utf-8-sig")
    print(f"   - 행 수: {len(df):,}")
    print(f"   - 열 수: {len(df.columns)}")

    return df


# ============================================================
# 2) Feature 생성
# ============================================================
def create_features(df: pd.DataFrame):

    print("\n🔧 [2단계] Feature 생성 시작")

    # ------------------------------------------------------------
    # 직원 관련 숫자형 안정 변환
    # ------------------------------------------------------------
    numeric_cols = ["총_직원수", "공인중개사수", "중개보조원수", "대표수", "일반직원수"]
    for col in numeric_cols:
        df[col] = pd.to_numeric(df[col], errors="coerce").fillna(0)

    # 비율 계산을 위한 안전값
    df["총_직원수_safe"] = df["총_직원수"].replace(0, 1)

    # ------------------------------------------------------------
    # 직원 구성 비율 Feature (실제 사용되는 것만)
    # ------------------------------------------------------------
    df["공인중개사_비율"] = df["공인중개사수"] / df["총_직원수_safe"]
    df["직원_자격증비율"] = df["공인중개사수"] / df["총_직원수_safe"]

    # ------------------------------------------------------------
    # 운영 기간 Feature (등록일 기준)
    # ------------------------------------------------------------
    df["등록일"] = pd.to_datetime(df["등록일"], errors="coerce")
    today = pd.Timestamp.now()
    df["운영기간_일"] = (today - df["등록일"]).dt.days.clip(lower=0)
    df["운영기간_년"] = df["운영기간_일"] / 365.25

    # ------------------------------------------------------------
    # 사무소 규모 Feature
    # ------------------------------------------------------------
    df["대형사무소"] = (df["총_직원수"] >= 3).astype(int)
    df["중형사무소"] = (df["총_직원수"] == 2).astype(int)
    df["소형사무소"] = (df["총_직원수"] == 1).astype(int)

    # ------------------------------------------------------------
    # 거래량 기반 Feature (실제 사용되는 것만)
    # ------------------------------------------------------------
    df["거래완료_safe"] = df["거래완료_숫자"].fillna(0)
    df["등록매물_safe"] = df["등록매물_숫자"].fillna(0)
    df["총거래활동량"] = df["거래완료_safe"] + df["등록매물_safe"]
    df["거래활동_강도"] = df["총거래활동량"] / df["총_직원수_safe"]
    df["직원당_거래완료"] = df["거래완료_safe"] / df["총_직원수_safe"]
    
    # ------------------------------------------------------------
    # 지역 기반 Feature (실제 사용되는 것만)
    # ------------------------------------------------------------
    df["지역내_거래완료순위"] = df.groupby("지역명")["거래완료_safe"].rank(pct=True)
    df["지역내_활동량순위"] = df.groupby("지역명")["총거래활동량"].rank(pct=True)
    df["지역_중개사수"] = df.groupby("지역명")["지역명"].transform("count")
    df["지역_경쟁강도"] = df["총거래활동량"] / df["지역_중개사수"]
    
    # 지역별 평균 성과로 지역 점수 생성
    region_performance = df.groupby("지역명")["총거래활동량"].mean()
    df["지역_성과점수"] = df["지역명"].map(region_performance)
    
    # ------------------------------------------------------------
    # 인력 효율성 Feature (실제 사용되는 것만)
    # ------------------------------------------------------------
    df["자격증_집중도"] = df["공인중개사수"] / df["총_직원수_safe"]
    df["직책_다양성"] = (
        (df["공인중개사수"] > 0).astype(int) +
        (df["중개보조원수"] > 0).astype(int) +
        (df["대표수"] > 0).astype(int) +
        (df["일반직원수"] > 0).astype(int)
    )
    df["팀규모_효과"] = np.where(df["총_직원수"] >= 3, 
                              df["총_직원수"] * 1.2,
                              df["총_직원수"])
    
    # ------------------------------------------------------------
    # 운영 경험 Feature (실제 사용되는 것만)
    # ------------------------------------------------------------
    df["운영경험_지수"] = np.exp(df["운영기간_년"] / 10)
    df["경험_인력_시너지"] = df["운영기간_년"] * df["총_직원수"]
    df["숙련도_지수"] = df["운영기간_년"] * df["자격증_집중도"]
    df["운영_안정성"] = np.where(df["운영기간_년"] >= 3, 1, 0)
    
    # ------------------------------------------------------------
    # 복합 지표 (실제 사용되는 것만)
    # ------------------------------------------------------------
    df["종합역량_지수"] = (
        df["총_직원수"] * 0.3 +
        df["운영기간_년"] * 0.4 +
        df["공인중개사수"] * 0.3
    )
    df["효율성_지수"] = df["총거래활동량"] / (df["총_직원수_safe"] + df["운영기간_년"] + 1)
    df["성장잠재력"] = (
        df["지역내_활동량순위"] * 0.4 +
        df["자격증_집중도"] * 0.3 +
        (1 / (df["운영기간_년"] + 1)) * 0.3
    )
    
    # ------------------------------------------------------------
    # 카테고리 Feature (실제 사용되는 것만)
    # ------------------------------------------------------------
    df["거래완료_구간"] = pd.qcut(df["거래완료_safe"], q=5, labels=False, duplicates='drop')
    df["활동량_구간"] = pd.qcut(df["총거래활동량"], q=5, labels=False, duplicates='drop')

    # ------------------------------------------------------------
    # 최종 Feature 선택 목록 (실제 사용되는 것만)
    # ------------------------------------------------------------
    # trust_model에서 실제 사용되는 피처들만 선택
    selected_features = [
        # 핵심 거래 지표
        "거래완료_safe", "등록매물_safe", "총거래활동량",
        "직원당_거래완료", "거래활동_강도",
        
        # 인력 핵심 지표
        "총_직원수", "공인중개사수", "공인중개사_비율", 
        "직원_자격증비율", "자격증_집중도",
        
        # 운영 경험
        "운영기간_년", "운영경험_지수", "경험_인력_시너지",
        "숙련도_지수", "운영_안정성",
        
        # 지역 경쟁력
        "지역내_거래완료순위", "지역내_활동량순위", 
        "지역_경쟁강도", "지역_성과점수",
        
        # 규모 효과
        "대형사무소", "중형사무소", "소형사무소",
        "팀규모_효과", "직책_다양성",
        
        # 복합 지표
        "종합역량_지수", "효율성_지수", "성장잠재력",
        
        # 구간화
        "거래완료_구간", "활동량_구간"
    ]
    
    # 실제 존재하는 Feature만 필터링
    feature_cols = [col for col in selected_features if col in df.columns]
    
    print(f"🎯 Feature 선별 완료:")
    print(f"   - 목표: 35개")
    print(f"   - 실제: {len(feature_cols)}개")

    # ------------------------------------------------------------
    # 숫자형만 필터링 → 문자열(ID 등) 자동 제거
    # ------------------------------------------------------------
    X = df[feature_cols].select_dtypes(include=[np.number])
    X = X.replace([np.inf, -np.inf], 0).fillna(0)

    # 타겟
    y = df["신뢰도등급"]

    print(f"   - 선별된 Feature 수: {len(X.columns)}개")
    print(f"   - X shape: {X.shape}")
    print(f"   - 결측치 총합: {X.isna().sum().sum()}")
    
    # 선별된 Feature 목록 출력
    print(f"\n📋 선별된 Feature 목록:")
    for i, feature in enumerate(X.columns, 1):
        print(f"   {i:2d}. {feature}")
    
    # 데이터 대비 Feature 비율 확인
    ratio = len(df) / len(X.columns)
    print(f"\n📊 데이터 효율성:")
    print(f"   - 데이터 수: {len(df)}개")
    print(f"   - Feature 수: {len(X.columns)}개") 
    print(f"   - 비율: {ratio:.1f} (권장: ≥10)")
    
    if ratio >= 10:
        print(f"   ✅ 적절한 Feature 수입니다!")
    else:
        print(f"   ⚠️ Feature가 많을 수 있습니다. (권장: {len(df)//10}개 이하)")

    return df, X, y, list(X.columns)


# ============================================================
# 3) Feature 저장
# ============================================================
def save_features(df, filepath="data/ML/office_features.csv"):
    Path(filepath).parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(filepath, index=False, encoding="utf-8-sig")
    print(f"\n💾 [3단계] Feature 포함 파일 저장 완료 → {filepath}")


# ============================================================
# main
# ============================================================
def main():
    print("🔧 Feature 생성 중...")

    df = load_target_data()
    df, X, y, feature_cols = create_features(df)
    save_features(df)

    print("✅ Feature 생성 완료\n")

    return df, X, y, feature_cols


if __name__ == "__main__":
    df, X, y, feature_cols = main()

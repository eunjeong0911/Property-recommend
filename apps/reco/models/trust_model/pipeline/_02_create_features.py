"""
02_create_features.py
중개사 특성 기반 Feature 생성 스크립트 (안정화 버전)
"""

import pandas as pd
import numpy as np
from pathlib import Path


# ============================================================
# 1) 타겟 데이터 로드
# ============================================================
def load_target_data(filepath: str = "data/office_target.csv") -> pd.DataFrame:
    file = Path(filepath)
    if not file.exists():
        raise FileNotFoundError(f"[ERROR] 파일이 존재하지 않음: {filepath}")

    print(f"📂 [1단계] 타겟 데이터 로드: {filepath}")
    df = pd.read_csv(file, encoding="utf-8-sig")
    print(f"   - 행 수: {len(df):,}")
    print(f"   - 열 수: {len(df.columns)}")

    return df


# ============================================================
# 2) Feature 생성
# ============================================================
def create_features(df: pd.DataFrame):

    print("\n🔧 [2단계] Feature 생성 시작")

    # ------------------------------------------------------------
    # 직원 관련 숫자형 안정 변환
    # ------------------------------------------------------------
    numeric_cols = ["총_직원수", "공인중개사수", "중개보조원수", "대표수", "일반직원수"]
    for col in numeric_cols:
        df[col] = pd.to_numeric(df[col], errors="coerce").fillna(0)

    # 비율 계산을 위한 안전값
    df["총_직원수_safe"] = df["총_직원수"].replace(0, 1)

    # ------------------------------------------------------------
    # 직원 구성 비율 Feature
    # ------------------------------------------------------------
    df["공인중개사_비율"] = df["공인중개사수"] / df["총_직원수_safe"]
    df["중개보조원_비율"] = df["중개보조원수"] / df["총_직원수_safe"]
    df["대표_비율"] = df["대표수"] / df["총_직원수_safe"]
    df["일반직원_비율"] = df["일반직원수"] / df["총_직원수_safe"]

    # ------------------------------------------------------------
    # 운영 기간 Feature
    # ------------------------------------------------------------
    df["개설시작일"] = pd.to_datetime(df["개설시작일"], errors="coerce")
    df["개설종료일"] = pd.to_datetime(df["개설종료일"], errors="coerce")

    today = pd.Timestamp.now()
    df["종료기준일"] = df["개설종료일"].fillna(today)

    df["운영기간_일"] = (df["종료기준일"] - df["개설시작일"]).dt.days.clip(lower=0)
    df["운영기간_년"] = df["운영기간_일"] / 365.25
    df["운영기간_일_log"] = np.log1p(df["운영기간_일"]).replace([np.inf, -np.inf], 0)

    # ------------------------------------------------------------
    # 자격증 관련 Feature
    # ------------------------------------------------------------
    df["복수공인중개사"] = (df["공인중개사수"] >= 2).astype(int)
    df["공인중개사_없음"] = (df["공인중개사수"] == 0).astype(int)
    df["직원_자격증비율"] = df["공인중개사수"] / df["총_직원수_safe"]

    # ------------------------------------------------------------
    # 사무소 규모 Feature
    # ------------------------------------------------------------
    df["대형사무소"] = (df["총_직원수"] >= 3).astype(int)
    df["중형사무소"] = (df["총_직원수"] == 2).astype(int)
    df["소형사무소"] = (df["총_직원수"] == 1).astype(int)

    # ------------------------------------------------------------
    # 운영기간 카테고리 Feature
    # ------------------------------------------------------------
    df["신규사무소"] = (df["운영기간_년"] <= 1).astype(int)
    df["중견사무소"] = ((df["운영기간_년"] > 1) & (df["운영기간_년"] <= 5)).astype(int)
    df["노포사무소"] = (df["운영기간_년"] > 5).astype(int)

    # ------------------------------------------------------------
    # 개설 월 패턴 Feature
    # ------------------------------------------------------------
    df["개설_월"] = df["개설시작일"].dt.month.fillna(0).astype(int)
    df["상반기개설"] = (df["개설_월"] <= 6).astype(int)
    df["하반기개설"] = (df["개설_월"] > 6).astype(int)

    # ------------------------------------------------------------
    # 클리핑 (이상치 억제)
    # ------------------------------------------------------------
    df["총_직원수_clip"] = df["총_직원수"].clip(upper=df["총_직원수"].quantile(0.99))
    df["운영기간_일_clip"] = df["운영기간_일"].clip(upper=df["운영기간_일"].quantile(0.99))

    # ------------------------------------------------------------
    # 최종 Feature 선택 목록
    # ------------------------------------------------------------
    feature_cols = [
        "총_직원수", "공인중개사수", "중개보조원수", "대표수", "일반직원수",
        "공인중개사_비율", "중개보조원_비율", "대표_비율", "일반직원_비율",
        "복수공인중개사", "공인중개사_없음", "직원_자격증비율",
        "대형사무소", "중형사무소", "소형사무소",
        "운영기간_일", "운영기간_년", "운영기간_일_log",
        "개설_월", "상반기개설", "하반기개설",
        "총_직원수_clip", "운영기간_일_clip",
        "신규사무소", "중견사무소", "노포사무소"
    ]

    # ------------------------------------------------------------
    # 숫자형만 필터링 → 문자열(ID 등) 자동 제거
    # ------------------------------------------------------------
    X = df[feature_cols].select_dtypes(include=[np.number])
    X = X.replace([np.inf, -np.inf], 0).fillna(0)

    # 타겟
    y = df["신뢰도등급"]

    print(f"   - 최종 Feature 수: {len(X.columns)}")
    print(f"   - X shape: {X.shape}")
    print(f"   - 결측치 총합: {X.isna().sum().sum()}")

    return df, X, y, list(X.columns)


# ============================================================
# 3) Feature 저장
# ============================================================
def save_features(df, filepath="data/office_features.csv"):
    Path(filepath).parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(filepath, index=False, encoding="utf-8-sig")
    print(f"\n💾 [3단계] Feature 포함 파일 저장 완료 → {filepath}")


# ============================================================
# main
# ============================================================
def main():
    print("=" * 70)
    print("🔧 02_create_features.py — Feature 생성 단계")
    print("=" * 70)

    df = load_target_data()
    df, X, y, feature_cols = create_features(df)
    save_features(df)

    print("\n✅ Feature 생성 완료!")
    print("=" * 70)

    return df, X, y, feature_cols


if __name__ == "__main__":
    df, X, y, feature_cols = main()
